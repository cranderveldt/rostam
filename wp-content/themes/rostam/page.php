      <h2 class="h2">Alternative Fee Arrangements</h2>
      <p>Rostam's fee arrangements are the new paradigm for legal billing. Fee arrangements are customized according to each client's distinct matter and preferences. Alternatively, Rostam offers standard hourly rates. The alternative arrangements can be broken down into several types such as a the Pure Contingency Fee, Partial Contingency Fee, Fixed Fee, Holdback/Success Fee, and other arrangements. However Rostam is not limited by these categories and is eager to create hybrid arrangements based on client concerns.</p>
      
      <h3 class="h3">Pure Contingency Fee</h3>
      <p>Pure contingency fee arrangements are traditional arrangements in which we receive a fixed or scaled percentage of recoveries from a client's lawsuit. These arrangements are often appropriate when the client has limited resources to finance its litigation.</p>
      
      <h3 class="h3">Partial Contingency Fee</h3>
      <p>Partial contingency fee arrangements are arrangements in which we receive a discounted portion of our hourly rates plus a smaller percentage of any recoveries or outcome in a client's matter.</p>
      
      <h3 class="h3">Fixed Fee or Flat Fee</h3>
      <p>Fixed of flat fee arrangements are arrangements in which we agree to handle a client's matter for a lump sum or a fixed monthly rate. These arrangements are often appropriate when the client desires to obtain more certainty with their legal budgets. The nature of the fixed or flat fee arrangement can be tailored to fit any given client matter.</p>
      
      <h3 class="h3">Holdback or Success Fee</h3>
      <p>Holdback or Success Fee arrangements are arrangements in which we are paid a portion of fees up front, with a portion withheld contingent upon the success of a matter. This fee structure is a great example of how we are able to minimize the legal and outcome risks for our clients.</p>
      
      <h3 class="h3">Other arrangements</h3>
      <p>We are not limited to any set of arrangements and are amendable to negotiate a fee arrangement that are customized to fit the client's needs. For example, we offer multiple matter arrangements in which we utilize differing fee arrangements based on the matter type.</p>
      
      <h2 class="h2">Charity Commitment</h2>

      <h3 class="h3">Pro Bono</h3>
      <p>Part of the Rostam culture is to act as quasi-public servants in the representation of the underprivileged, oppressed, and defenseless. Rostam extents a requirement of the California attorney oath to all its attorneys: never to reject, for any consideration personal to himself or herself, the cause of the defenseless or the oppressed. Rostam seeks to ingrain this duty into the minds of our lawyers. We encourage our lawyers to accept pro bono representation whenever the opportunity presents itself. In fact, Rostam mandates that its attorneys satisfy any aspirational pro bono commitment in the states they are licensed to practice.</p>
      

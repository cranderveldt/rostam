      <p>Rostam law is a virtual general practice law firm. This means we provide experienced attorneys that fit most legal needs without the unwanted accouterments of traditional law firms. Specifically, we do not provide our attorneys with mandatory salaries and offices. Instead, our attorneys work remotely or at the client’s office, and are only paid when actually providing legal services. This is a manifestation of our dedication to a lean structure.</p>
